
namespace DaDaYaRemastered
{
    public class CollectionEstates
    {
        
        int id;
        int price;
        int square;
        int date;
        string estateName;
        string city;
        string adress;

        public int Id { get => id; set => id = value; }
        public int Price { get => price; set => price = value; }
        public int Square { get => square; set => square = value; }
        public int Date { get => date; set => date = value; }
        public string EstateName { get => estateName; set => estateName = value; }
        public string City { get => city; set => city = value; }
        public string Adress { get => adress; set => adress = value; }
    }
}
